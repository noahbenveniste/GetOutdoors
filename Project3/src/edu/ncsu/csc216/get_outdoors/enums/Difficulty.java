package edu.ncsu.csc216.get_outdoors.enums;

/**
 * An enumeration utilized by the Trail class. Indicates the different levels
 * of difficulty a Trail can have.
 * 
 * @author Noah Benveniste
 */
public enum Difficulty {
	
	/** Lowest difficulty level*/
	EASY,
	/** Moderate difficulty */
	MODERATE,
	/** Challenging difficulty */
	CHALLENGING,
	/** Middle difficulty level */
	DIFFICULT,
	/** Very difficult difficulty */
	VERY_DIFFICULT,
	/** Highest difficulty level */
	EXTREME;
	
}